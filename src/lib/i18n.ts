/**
 * 번역 키로 텍스트를 반환하는 React 훅. `language` 설정을 자동으로 읽는다.
 *
 * 사용:
 *   const t = useT();
 *   t("action.save")                   // "저장" or "Save"
 *   t("task.count", { n: 5 })          // "5개의 업무" or "5 tasks"
 *   t(`error.${code}`)                 // API 에러 코드 번역
 */
export function useT() {
  const { labelLanguage } = useSettings();
  return useCallback(
    (key: string, vars?: Record<string, string | number>): string => {
      const lang = labelLanguage as Lang;
      const val = dict[lang]?.[key] ?? dict.local[key] ?? key;
      if (!vars) return val;
      return Object.entries(vars).reduce(
        (s, [k, v]) => s.replace(`{${k}}`, String(v)),
        val,
      );
    },
    [labelLanguage],
  );
}

/**
 * constants.ts의 label/label_en 쌍을 labelLanguage에 따라 반환하는 훅.
 * 사용: const lbl = useLabel(); lbl(TASK_STATUSES[i]) → "To Do" or "할 일"
 */
export function useLabel() {
  const { labelLanguage } = useSettings();
  return useCallback(
    <T extends { label: string; label_en: string }>(item: T): string =>
      labelLanguage === "en" ? item.label_en : item.label,
    [labelLanguage],
  );
}

/**
 * React 훅 외부에서 특정 언어로 직접 번역할 때 사용.
 * 컴포넌트 내부에서는 반드시 `useT()` 훅을 사용한다.
 */
export function translate(key: string, lang: Lang = "local"): string {
  return dict[lang]?.[key] ?? dict.local[key] ?? key;
}
